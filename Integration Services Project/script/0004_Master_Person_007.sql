AS
BEGIN
	declare @v_start_id int;
	declare @v_end_id int;
	declare @v_cnt int;
	declare @v_max_id bigint;
	declare @PackageName varchar(250);

	-- update maximo seq
	select @v_max_id=max(PERSONUID) from PERSON;
	update maxsequence set maxreserved=@v_max_id+1 where tbname='PERSON' and name='PERSONUID';

	-- get package name
	select @PackageName = package_name from [dbo].[ste_migration_logs] where id = @PackageLogID;
	if (@PackageName is null) return;

	-- update start_id and end_id for ITEM_
	select @v_cnt=count(STE_MIGRATIONID) from person
	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'RES_EMPL' AND STATUS = 'ACTIVE';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
		,[log_id]
		,[event]
		,[event_type]
		,[event_description]
	)
	values (
		@PackageName
		, @PackageLogID
		, 'RES_EMPL (ACTIVE)'
		, 'LOG'
		, CONCAT('COUNT: ', @v_cnt)
	);

	select @v_cnt=count(STE_MIGRATIONID) from person
	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'RES_EMPL' AND STATUS = 'INACTIVE';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
		,[log_id]
		,[event]
		,[event_type]
		,[event_description]
	)
	values (
		@PackageName
		, @PackageLogID
		, 'RES_EMPL (RESIGNED)'
		, 'LOG'
		, CONCAT('COUNT: ', @v_cnt)
	);

	select @v_start_id=min(STE_MIGRATIONID), @v_end_id=max(STE_MIGRATIONID), @v_cnt=count(STE_MIGRATIONID) from person
	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'RES_EMPL';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
		,[log_id]
		,[event]
		,[event_type]
		,[event_description]
	)
	values (
		@PackageName
		, @PackageLogID
		, 'RES_EMPL'
		, 'COMPLETED'
		, CONCAT('COUNT: ', @v_cnt, ', START_ID: ', @v_start_id, ', END_ID: ', @v_end_id)
	);

	select @v_start_id=min(STE_MIGRATIONID), @v_end_id=max(STE_MIGRATIONID), @v_cnt=count(STE_MIGRATIONID) from person
	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'WIN_USER';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
		,[log_id]
		,[event]
		,[event_type]
		,[event_description]
	)
