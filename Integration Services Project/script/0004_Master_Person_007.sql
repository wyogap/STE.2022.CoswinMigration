	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'WO (WO_REP_BY)';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
		,[log_id]
		,[event]
		,[event_type]
		,[event_description]
	)
	values (
		@PackageName
		, @PackageLogID
		, 'WO-REPORTEDBY'
		, 'COMPLETED'
		, CONCAT('COUNT: ', @v_cnt, ', START_ID: ', @v_start_id, ', END_ID: ', @v_end_id)
	);

	select @v_start_id=min(STE_MIGRATIONID), @v_end_id=max(STE_MIGRATIONID), @v_cnt=count(STE_MIGRATIONID) from person
	where STE_MIGRATIONID is not null and STE_MIGRATIONSOURCE = 'SR (JR_REQBY_ID)';

	insert into [dbo].[ste_migration_log_details] (
		[package_name]
